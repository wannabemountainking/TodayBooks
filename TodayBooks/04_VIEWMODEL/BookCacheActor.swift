import Foundation

// MARK: - 캐시 데이터 구조


// MARK: - 캐시 전담 관리 Actor
/// 스레드면에서 안전한 (ThreadSafe) 캐시 관리 Actor
///
/// Actor 의 역할
/// - API 응답을 메모리에 캐싱하여 중복 호출 방지
/// - 여러 ViewModel이 동시에 접근해도 스레드 안전하게 관리
/// - 캐시 만료 시간(TTL) 관리로 오래된 데이터 자동 제거
///
/// 왜 Actor 인가?
/// - 여러 스레드에서 동시 접근 가능
/// - Actor가 자동으로 접근을 직렬화함 (순차 처리)
/// - 데이터 레이스(data racing) 조건 완벽 방지
///
/// 🔄 Actor 캐시와의 협업 구조
/// ┌─────────────┐  1. 요청    ┌──────────┐
/// │  ViewModel  │ ─────────→ │  Actor   │
/// │ (메인 스레드)  |            │ (캐시)    │
/// └─────────────┘            └──────────┘
///       ↑                          ↓
///       │                     2. 캐시 확인
///       │                          ↓
///       │                    Cache Hit?
///       │                     ↙        ↘
///       │               YES (즉시)   NO
///       │                 ↓            ↓
///       │            캐시 반환      ┌──────────┐
///       │                 ↓       │ Service  │
///       │                         │   (API)  │
///       │                         └──────────┘
///       │                               ↓
///       │                          3. 캐시 저장
///       │                               ↓
///       └───────────────────────────────┘
///              4. ViewModel 업데이트 + UI 갱신
